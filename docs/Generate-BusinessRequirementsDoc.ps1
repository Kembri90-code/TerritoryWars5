function Set-NormalStyle([object]$doc) {
    $doc.Styles(-95).Font.Name = 'Calibri'
    $doc.Styles(-95).Font.Size = 11
    $doc.Styles(-2).Font.Name = 'Calibri'; $doc.Styles(-2).Font.Size = 16
    $doc.Styles(-3).Font.Name = 'Calibri'; $doc.Styles(-3).Font.Size = 14
    $doc.Styles(-4).Font.Name = 'Calibri'; $doc.Styles(-4).Font.Size = 12
}

function Add-H([object]$sel, [object]$doc, [int]$level, [string]$text) {
    $wd = switch ($level) { 1 {-2} 2 {-3} 3 {-4} 4 {-5} Default {-4} }
    $sel.Style = $doc.Styles($wd)
    $sel.TypeText($text)
    $sel.TypeParagraph()
    $sel.Style = $doc.Styles(-95)
}

function Add-P([object]$sel, [string]$text) {
    if ([string]::IsNullOrEmpty($text)) { return }
    $sel.TypeText($text)
    $sel.TypeParagraph()
}

function New-TableFull([object]$doc, [object]$sel, [string[][]]$rows) {
    if ($rows.Count -lt 1) { return }
    $rCount = $rows.Count; $cCount = $rows[0].Count
    $t = $doc.Tables.Add($sel.Range, $rCount, $cCount)
    for ($i = 0; $i -lt $rCount; $i++) {
        for ($j = 0; $j -lt $cCount; $j++) {
            $t.Cell($i + 1, $j + 1).Range.Text = $rows[$i][$j]
        }
    }
    try { $t.Style = $doc.Styles(-266) } catch {}
    try {
        $t.Rows(1).Shading.BackgroundPatternColor = 15132390
        $t.Rows(1).Range.Font.Bold = $true
    } catch {}
    $sel.EndKey(6)
    $sel.TypeParagraph()
}

function Add-ScreenBlock {
    param(
        [object]$doc, [object]$sel,
        [string]$heading,
        [string]$imageFileName,
        [string[]]$descriptionParagraphs,
        [string[][]]$elementRows
    )
    Add-H $sel $doc 2 $heading
    $imgPath = Join-Path $imgDir $imageFileName
    if (-not (Test-Path $imgPath)) {
        Add-P $sel "ОШИБКА: файл иллюстрации не найден: $imgPath"
        return
    }

    $t = $doc.Tables.Add($sel.Range, 1, 2)
    try { $t.Borders.Enable = $true } catch {}
    $t.Rows(1).Cells(1).VerticalAlignment = $wdCellAlignVerticalTop
    $t.Rows(1).Cells(2).VerticalAlignment = $wdCellAlignVerticalTop
    $t.Columns(1).PreferredWidthType = 3
    $t.Columns(1).PreferredWidth = 230
    $t.Columns(2).PreferredWidthType = 3
    $t.Columns(2).PreferredWidth = 300

    $r1 = $t.Cell(1, 1).Range
    $r1.Text = ""
    $r1.Select()
    $pic = $sel.InlineShapes.AddPicture($imgPath, $false, $true)
    if ($pic.Width -gt 220) {
        $pic.LockAspectRatio = $true
        $pic.Width = 220
    }
    $sel.TypeParagraph()
    $sel.Font.Size = 9
    $sel.Font.Italic = $true
    $sel.TypeText('Иллюстрация составлена по элементам интерфейса, реализованным в приложении. При необходимости замените на фотографию экрана с устройства.')
    $sel.TypeParagraph()
    $sel.Font.Italic = $false
    $sel.Font.Size = 11

    $r2 = $t.Cell(1, 2).Range
    $r2.Text = ""
    $r2.Select()
    foreach ($para in $descriptionParagraphs) {
        if (-not [string]::IsNullOrEmpty($para)) {
            $sel.TypeText($para)
            $sel.TypeParagraph()
        }
    }

    $sel.EndKey(6)
    $sel.TypeParagraph()

    if ($elementRows -and $elementRows.Count -gt 0) {
        Add-H $sel $doc 4 "Таблица элементов экрана: $heading"
        New-TableFull $doc $sel $elementRows
    }
}

